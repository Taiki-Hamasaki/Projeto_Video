    public void atualizaTabela() {
        listaUsuarios.clear();
        listaUsuarios.addAll(daoUsuario.getLista());
        int linha = listaUsuarios.size()-1;
        if(linha >= 0) {
            tblUsuarios.setRowSelectionInterval(linha, linha);
            tblUsuarios.scrollRectToVisible(tblUsuarios.getCellRect(linha, linha, true));
        } else {
            btnPrimeiro.setEnabled(false);
            btnAnterior.setEnabled(false);
            btnProximo.setEnabled(false);
            btnUltimo.setEnabled(false);
            txtCodUsuario.setText("");
            txtUsuario.setText("");
            txtEmail.setText("");
            campoSenha.setText("");
            campoConfirmarSenha.setText("");
            
            // Limpar caixa de pesquisa
            txtPesquisa.setText("");
        }
    }
    
    public void trataEdicao(boolean editando) {
        btnExcluir.setEnabled(editando);
        btnSalvar.setEnabled(editando);
        btnEditar.setEnabled(!editando);
        
        btnPrimeiro.setEnabled(!editando);
        btnProximo.setEnabled(!editando);
        btnAnterior.setEnabled(!editando);
        btnUltimo.setEnabled(!editando);
        
        int linha = listaUsuarios.size() - 1;
        if(linha < 0) {
            btnPrimeiro.setEnabled(false);
            btnProximo.setEnabled(false);
            btnAnterior.setEnabled(false);
            btnUltimo.setEnabled(false);
            btnEditar.setEnabled(false);
            
            txtCodUsuario.setText("");
            txtUsuario.setText("");
            txtEmail.setText("");
            campoSenha.setText("");
            campoConfirmarSenha.setText("");
        } else {
            btnExcluir.setEnabled(!editando);
        }
        btnNovo.setEnabled(!editando);
        btnSair.setEnabled(!editando);
        
        txtCodUsuario.setEnabled(editando);
        txtUsuario.setEnabled(editando);
        txtEmail.setEnabled(editando);
        campoSenha.setEnabled(editando);
        campoConfirmarSenha.setEnabled(editando);
        tblUsuarios.setEnabled(editando);
    }
    
    public boolean validaCampos() {
        if (!(txtUsuario.getText().length() > 0)) {
            JOptionPane.showMessageDialog(null, "Informe seu Nome Completo");
            txtUsuario.requestFocus();
            return false;
        }
        if (!(txtEmail.getText().length() > 0)) {
            JOptionPane.showMessageDialog(null, "Informe seu Email Completo");
            txtEmail.requestFocus();
            return false;
        }
        if (!(campoSenha.getPassword().length > 0)) {
            JOptionPane.showMessageDialog(null, "Adicione uma senha à sua conta");
            campoSenha.requestFocus();
            return false;
        }
        if (!(campoConfirmarSenha.getPassword().length > 0)) {
            JOptionPane.showMessageDialog(null, "Por favor confirme a sua senha");
            campoConfirmarSenha.requestFocus();
            return false;
        }
        if (!(String.valueOf(campoSenha.getPassword()).equals(String.valueOf(campoConfirmarSenha.getPassword())))) {
            JOptionPane.showMessageDialog(null, "Sua senha não coincide:\nPor favor, insira novamente");
            campoConfirmarSenha.requestFocus();
            return false;
        }
        return true;
    }
    

