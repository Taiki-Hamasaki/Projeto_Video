    private void txtPesquisaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPesquisaKeyReleased
        if(txtPesquisa.getText().length() > 0) {
            listaUsuarios.clear();
            listaUsuarios.addAll(daoUsuario.pesquisa(txtPesquisa.getText()));
            int linha = listaUsuarios.size()-1;
            if(linha >= 0) {
                tblUsuarios.setRowSelectionInterval(linha, linha);
                tblUsuarios.scrollRectToVisible(tblUsuarios.getCellRect(linha, linha, true));
            }
        } else {
            atualizaTabela();
        }
    }//GEN-LAST:event_txtPesquisaKeyReleased

