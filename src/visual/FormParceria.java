    private void mudancas(int do_or_undo, Canal c) {
        if(do_or_undo == 0) {
            listCanal.remove(c);
            listParceiros.add(c);
        } else if(do_or_undo == 1) {
            listCanal.add(c);
            // Retorna para a lista de Canais disponiveis, ordenando de acordo com o getCodCanal()
            // na ordem que foi criado no bd
            listCanal.sort(Comparator.comparing(Canal::getCodCanal));
            listParceiros.remove(c);
        }
    }
    
    private void trataEdicao(boolean editando) {
        btnFinalizar.setEnabled(editando);
        btnCancelar.setEnabled(editando);
        jListCanais.setEnabled(editando);
        jListParceiros.setEnabled(editando);
        txtEmpresa.setEnabled(editando);
        txtDataInicio.setEnabled(editando);
        txtDataFim.setEnabled(editando);
        btnContratarParceria.setEnabled(editando);
        btnRemove.setEnabled(editando);
        if(editando) {
            txtEmpresa.setText("");
            listCanal.clear();
            listCanal.addAll(daoCanal.getLista());
            listParceiros.clear();
        }
    }
    
    private boolean validaCampos() {
        if(!(txtEmpresa.getText().length() >= 0)) {
            JOptionPane.showMessageDialog(null, "Informe o nome da empresa");
            return false;
        }
        
        if(!(jListParceiros.getSelectedIndex() >= 0)) {
            JOptionPane.showMessageDialog(null, "Informe o Canal parceiro");
            return false;
        }
        return true;
    }

