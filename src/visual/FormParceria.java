    private void btnContratarParceriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnContratarParceriaActionPerformed
        if(!(jListCanais.getSelectedIndex() >= 0)) {
            JOptionPane.showMessageDialog(null, "Selecione um canal para parceiro");
            jListCanais.requestFocus();
        } else {
            int linhaSelecionada = jListCanais.getSelectedIndex();
            Canal c = listCanal.get(linhaSelecionada);
            mudancas(0, c);
        }
    }//GEN-LAST:event_btnContratarParceriaActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        if (!(jListParceiros.getSelectedIndex() >= 0)) {
            JOptionPane.showMessageDialog(null, "Nenhum canal a ser removido");
            jListParceiros.requestFocus();

        } else {
            int linhaSelecionada = jListParceiros.getSelectedIndex();
            Canal c = listParceiros.get(linhaSelecionada);
            mudancas(1, c);
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        listParceiros.clear();
        listCanal.clear();
        listCanal.addAll(daoCanal.getLista());
        trataEdicao(true);
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinalizarActionPerformed
        if(validaCampos()) {
            objParceria.setNomeEmpresa(txtEmpresa.getText());
            objParceria.setInicioContrato(converteDataWeb.converteCalendario(txtDataInicio.getText()));
            objParceria.setFimContrato(converteDataWeb.converteCalendario(txtDataFim.getText()));
            objParceria.setCanalParceiro((Canal) jListParceiros.getSelectedValue());
            daoParcerias.incluir(objParceria);
            listParceiros.remove(jListParceiros.getSelectedIndex());
            mudancas(1, objParceria.getCanalParceiro());
            if(!(listParceiros.size() >= 0)) {
                trataEdicao(false);
            }
        }
    }//GEN-LAST:event_btnFinalizarActionPerformed

    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
        trataEdicao(false);
        txtDataInicio.setText("");
        txtDataFim.setText("");
        listCanal.clear();
        listCanal.addAll(daoCanal.getLista());
        listParceiros.clear();
    }//GEN-LAST:event_btnCancelarActionPerformed

