    private void mostrarSenhaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mostrarSenhaActionPerformed
        if(campoSenha.getEchoChar() != '\u2022') {
            campoSenha.setEchoChar('\u2022');
        } else {
            campoSenha.setEchoChar((char) 0);
        }
    }//GEN-LAST:event_mostrarSenhaActionPerformed

    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
        int opcao = JOptionPane.showOptionDialog(null, "Cadastrar usuário ou sair?", "Pergunta",
            JOptionPane.YES_OPTION, JOptionPane.QUESTION_MESSAGE, null, new String[]{"Sign In", "Continuar"}, "Sign In");
        if(opcao == 0) {
            dispose();
            CadastroUsuario cadUser = new CadastroUsuario(null, true);
            cadUser.setDefaultCloseOperation(JDialog.DO_NOTHING_ON_CLOSE);
            cadUser.setLocationRelativeTo(null);
            cadUser.setVisible(true);
        }
    }//GEN-LAST:event_btnCancelarActionPerformed

    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        if(validaCampos()) {
            FormPrincipal.usuarioLogado = daoUsuario.login(txtUsuarioOuEmail.getText(), String.valueOf(campoSenha.getPassword()));
            txtUsuarioOuEmail.setEditable(false);
            campoSenha.setEditable(false);
            dispose();
        }
    }//GEN-LAST:event_btnEntrarActionPerformed

