    public boolean validaCampos() {
        if(!(txtUsuarioOuEmail.getText().length() > 0)) {
            JOptionPane.showMessageDialog(null, "Informe o nome de usuário ou email");
            txtUsuarioOuEmail.requestFocus();
            return false;
        }
        if(!(campoSenha.getPassword().length > 0)) {
            JOptionPane.showMessageDialog(null, "Informe a sua senha");
            campoSenha.requestFocus();
            return false;
        }
        return true;
    }

